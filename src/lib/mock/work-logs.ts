export function getWorkLogsForCaregiver(
  caregiverId: string = 'cg-001',
  contractId?: string,
): WorkLog[] {
  return mockWorkLogs
    .filter((w) => w.caregiver_id === caregiverId)
    .filter((w) => (contractId ? w.contract_id === contractId : true))
    .sort((a, b) => b.date.localeCompare(a.date));
}

export function getWorkLogsForContract(contractId: string): WorkLog[] {
  return mockWorkLogs
    .filter((w) => w.contract_id === contractId)
    .sort((a, b) => b.date.localeCompare(a.date));
}
